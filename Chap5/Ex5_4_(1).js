// 예제 5-4 return 없이도 클로저가 발생하는 다양한 경우
// (1) setInterval/setTimeout
(function () {
    var a = 0;
    var intervalId = null;
    var inner = function () {
        if (++a >= 10) {
            clearInterval(intervalId);
        }
        console.log(a);
    };
    intervalId = setInterval(inner, 1000);
})();


